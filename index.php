							<p>Current tools provide specific functionalities like customized transfer recommendations, points, projections, etc. using past statistics but most of them require a subscription.
							Related work include <a href="https://www.fantasyfootballfix.com">Fantasy Football Fix</a>, which has features like predicting which players will change in value and when, showing player points projects based on past statistics. The work suffers from the drawback that the onus is on user to create his own team and try out different team combinations based on points predicted and optimizations suggested. <a href="http://www.fantasyfootballgeek.co.uk">Fantasy Football Geek</a> and <a href="https://www.fantasyfootballscout.co.uk">Scout</a> post news, tips and tricks, player rankings, game analysis, etc based on expert opinions but do not provide actual recommendations that our project aims to do.</p>
							<p>Our project provides recommendations based on players data from recent and past performances. We provide recommendations on different categories, like top 10 attackers, defenders, midfielders and goalkeepers, which will help the user to build his own team. Unlike other subscription based solutions, we aim to make our project free for all soccer fans. By providing recommendations in each category, we provide a simplistic and easy to use solution for everyone. These differences certainly prove to be beneficial to users..</p>
							
